/** Progression bornée 0–1 d'un objectif. */
export function goalProgress(goal: GoalDTO): number {
    if (goal.targetAmount <= 0) return 0;
    return Math.min(goal.savedAmount / goal.targetAmount, 1);
}

export function goalReached(goal: GoalDTO): boolean {
    return goal.savedAmount >= goal.targetAmount;
}
